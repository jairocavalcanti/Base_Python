# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. 
# Calcule e mostre o total do seu salário no referido mês.

quantia = int(input("Insira a quantidade ganha por horas: "))

print("Quantidade ganha por horas: " , quantia)

horas = int(input("Insira a quantidade de horas trabalhadas no mes: "))

print("Quantidade de horas trabalhadas: " , horas)

mensal = (quantia*horas)

print("Ganho mensal: ", mensal)